void Landscape::render(int cameraXPos)
{
	int xPos = cameraXPos - 64;
	if (xPos < 0)
	{
		xPos += 1024;
	}

	for (int i = 0; i < 128; i++)
	{
		arduboy.drawPixel(i, pgm_read_byte(landscapeData + xPos++));
		if (xPos == 1024)
		{
			xPos = 0;
		}
	}
}

byte Landscape::getHeight(int xPos)
{
	return pgm_read_byte(landscapeData + xPos);
}

